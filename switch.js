var color = "Red";

switch (color) {
    case "red":console.log("Color is Red");
    break;
    case "Grean":console.log("Color is Grean");
    break;
    case "Blue":console.log("Color is Blue");
    break;
    case "Yellow":console.log("Color is Yellow");
    break;
    default:console.log("Color is Black")
}